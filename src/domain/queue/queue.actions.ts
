// ── Helper: التحقق من المستخدم ───────────────────────────
async function getAuthContext() {
  const supabase = await createClient();
  const { data: { user } } = await supabase.auth.getUser();
  if (!user) throw new Error("Not authenticated");

  const tenantId = user.user_metadata?.tenant_id as string | undefined;
  if (!tenantId) throw new Error("No tenant assigned");

  return { supabase, user, tenantId, userId: user.id };
}

// ── Helper: التحقق من صلاحية الانتقال ─────────────────────
function validateStatusTransition(
  current: SessionStatus,
  newStatus: SessionStatus
): { valid: boolean; reason?: string } {
  const allowed: Record<SessionStatus, SessionStatus[]> = {
    waiting: ["in_consultation", "no_show", "cancelled"],
    in_consultation: ["pending_close", "cancelled"],
    pending_close: ["completed", "cancelled"],
    completed: [],
    cancelled: [],
    no_show: [],
  };

  if (allowed[current]?.includes(newStatus)) {
    return { valid: true };
  }

  return {
    valid: false,
    reason: `Cannot transition from ${current} to ${newStatus}`,
  };
}

// ── 1. تسجيل حضور مريض (Check-in) ─────────────────────────
export async function checkInPatient(data: {
  patient_id: string;
  doctor_id?: string;
  room_id?: string;
  agenda_event_id?: string;
  notes?: string;
}): Promise<EnrichedSession> {
  const { supabase, tenantId, userId } = await getAuthContext();

  const insertData = {
    tenant_id: tenantId,
    patient_id: data.patient_id,
    doctor_id: data.doctor_id || null,
    room_id: data.room_id || null,
    agenda_event_id: data.agenda_event_id || null,
    session_status: "waiting" as SessionStatus,
    created_at: new Date().toISOString(),
    updated_at: new Date().toISOString(),
  };

  const { data: session, error } = await supabase
    .from("clinic_visit_sessions")
    .insert(insertData)
    .select()
    .single();

  if (error) throw new Error(`Check-in failed: ${error.message}`);

  revalidatePath("/(dashboard)/queue");
  return session as EnrichedSession;
}

// ── 2. استدعاء مريض (Call Next) ────────────────────────────
export async function callNextPatient(sessionId: string): Promise<EnrichedSession> {
  const { supabase, tenantId, userId } = await getAuthContext();

  const { data: current } = await supabase
    .from("clinic_visit_sessions")
    .select("session_status")
    .eq("id", sessionId)
    .eq("tenant_id", tenantId)
    .single();

  if (!current) throw new Error("Session not found");

  const validation = validateStatusTransition(current.session_status as SessionStatus, "in_consultation");
  if (!validation.valid) throw new Error(validation.reason);

  const { data: session, error } = await supabase
    .from("clinic_visit_sessions")
    .update({
      session_status: "in_consultation",
      lock_holder_id: userId,
      lock_timestamp: new Date().toISOString(),
      updated_at: new Date().toISOString(),
    })
    .eq("id", sessionId)
    .eq("tenant_id", tenantId)
    .select()
    .single();

  if (error) throw new Error(`Call patient failed: ${error.message}`);

  revalidatePath("/(dashboard)/queue");
  return session as EnrichedSession;
}

